        String s1=User_Name.getText();
        String s2=Password.getText();
        String User_Name=s1.toUpperCase();
        String password=s2.toUpperCase();
        
        if (l.Check_Login(User_Name,password)==1)
        {
            new AskUser().setVisible(true);
            this.setVisible(false);
        }
        else if (l.Check_Login(User_Name,password)==2) {
            new ResetPassword().setVisible(true);
            this.setVisible(false);
        }
