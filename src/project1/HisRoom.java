        public void Show_Data()
    {
        try {
            c=t.setconnect();
            ss=c.createStatement();
            query="select * from reservation";
            ResultSet r=ss.executeQuery(query);
            DefaultTableModel mm=(DefaultTableModel)table.getModel();
            Object [] row = new Object[7];
            while (r.next())
            {
                if (r.getInt("SSN")==ssn_c)
                {
                    row [0]= r.getString("Name");
                    row [1]= r.getInt("SSN");
                    row [2]= r.getInt("Phone");
                    row [3]= r.getInt("Duration");
                    row [4]= r.getString("Room_ID");
                    row [5]= r.getString("Service");
                    row [6]= r.getString("Check_In");
                    
                    mm.addRow(row);
                }
            }
        }catch (SQLException e)
        {
            System.out.println(e.getMessage());
        }
                finally {
            try {
                c.close();
                ss.close();
            }              
            catch(SQLException ee) {
                System.out.println(ee.getMessage());
            }
        }   
    }

    
